int	ft_recursive_power(int nb, int power)
{
	int	result;

	if (power < 0)
		return (0);
	result = 1;
	while (power > 0)
	{
		result *= nb;
		power -= 1;
		ft_recursive_power(nb, power);
	}
	return (result);
}
